    private void botonPrincipal1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPrincipal1ActionPerformed
        String nombre= inputNombreRecep.getText();
       String sexo = inputSexoRecep.getText();
       String dni = inputDniRecep.getText();
       String nac = inputNacRecep.getText();
       String dto =inputDepRecep.getText();
 
       Recepcionista recep= new Recepcionista();
           recep.setAreaAtencion(dto);
       recep.setNombre(nombre);
       recep.setSexo(sexo);
       recep.setDni(dni);
       recep.setNacionalidad(nac);
   
       
       
       listaRecepcionista.agregar(recep);
       JOptionPane.showMessageDialog(null, "Se ha guardado correctamente");
        
        

        inputNombreRecep.setText("");
        inputSexoRecep.setText("");
        inputDniRecep.setText("");
        inputNacRecep.setText("");
        inputDepRecep.setText("");
        
        
        
        
    }//GEN-LAST:event_botonPrincipal1ActionPerformed

