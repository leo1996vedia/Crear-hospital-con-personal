    private void botonPrincipal2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonPrincipal2ActionPerformed
        String nombre= inputNombreAdmin.getText();
        String sexo = inputSexoAdmin.getText();
        String dni = inputDniAdmin.getText();
        String nac = inputNacAdmin.getText();
        String dto =inputDepAdmin.getText();
        
        

        Administracion admin= new Administracion();
          admin.setDepartamento(dto);
        admin.setNombre(nombre);
        admin.setSexo(sexo);
        admin.setDni(dni);
        admin.setNacionalidad(nac);
      

        listaAdmin.agregar(admin);
        JOptionPane.showMessageDialog(null, "Se ha guardado correctamente");
        
        
        inputNombreAdmin.setText("");
        inputSexoAdmin.setText("");
        inputDniAdmin.setText("");
        inputNacAdmin.setText("");
        inputDepAdmin.setText("");
        
        
    }//GEN-LAST:event_botonPrincipal2ActionPerformed


